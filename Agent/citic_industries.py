def build_industry_tag_groups() -> Dict[str, Dict[str, Any]]:
    """
    构建行业标签组（从简化结构生成）
    
    Returns:
        {一级行业名: {
            'name': 一级行业名,
            'aliases': [所有别名/同义词列表（去重）]
        }}
    """
    tag_groups = {}
    
    for level1_name, synonyms in CITIC_INDUSTRY_TAG_GROUPS.items():
        # 去重并保持顺序（一级行业名称在前）
        unique_synonyms = []
        seen = set()
        
        # 先添加一级行业名称
        if level1_name not in seen:
            unique_synonyms.append(level1_name)
            seen.add(level1_name)
        
        # 添加其他同义词
        for synonym in synonyms:
            if synonym and synonym not in seen:
                unique_synonyms.append(synonym)
                seen.add(synonym)
        
        tag_groups[level1_name] = {
            'name': level1_name,
            'aliases': unique_synonyms
        }
    
    return tag_groups


